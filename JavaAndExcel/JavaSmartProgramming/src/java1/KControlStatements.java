package java1;

public class KControlStatements {

	public static void main(String[] args) {
		/*
		 * a java program normally executes from top to bottom
		 * but if we want to control order of execution of program based on logic
		 * and values we use control statements
		 *
		 * there 3 types
		 * 1.selection or conditional statements
		 * 		if , if else , if else if else , nested if  , switch
		 *
		 * 2.Iteration or looping statements
		 * 		for while do while
		 * 3.Jump statements
		 * 		break  continue return goto
		 *
		 *
		 * 1.selection or conditional statements
		 * 		if , if else , if else if else , nested if  , switch
		 *
		 * selection statements allows us to control the flow of program execution
		 * on basis of outcome of an expression(i.e condition)
		 * if(condition){
		 *
		 * }
		 *
		 *
		 * if(condition){
		 * 		//if condition is true
		 * }else{
		 *
		 * }
		 *
		 *
		 * if(condition){
		 *
		 * }else if(condition){
		 *
		 * }else{
		 *
		 * }
		 *
		 *
		 * if(condition){
		 * 			if(condition){
		 *
		 * 			}
		 * }
		 *
		 *
		 * switch(variable to be tested){
		 * 		case value1:
		 * 				body
		 * 				break;
		 * 		case value2:
		 * 				body
		 * 				break;
		 * 		default:
		 * 			body
		 *
		 *  2.Iteration or looping statements
		 * 		for while do while
		 *
		 * for(initialization ; condition ; inc/dec){
		 * 			//body
		 * }
		 *
		 * for(int a =1;a<10;a++){
		 *
		 * }
		 * flow of execution of for loop
		 * 1st initialization , then checks condition and executes body , then increment or decrement
		 *
		 * while(condition){
		 * 		//body
		 * }
		 *
		 *
		 * do{
		 * 		//body
		 * }while(condition);
		 *
		 *  while - based on condition true body will execute
		 *  do while - first it will execute once then checks the condition
		 *
		 *
		 *   * 3.Jump statements
		 * 		break  continue return goto
		 *
		 * return statement is used to immdeiately quit current method and return
		 * to calling method . it is mandatory to use a return statement for non void
		 * methods to return a value
		 *
		 * break statement immediately quits current iteration and goes to first statement
		 * following the loop another form of break is used in switch statement
		 *
		 * continue statement is used when u want to continue running loop with the next
		 * iteration and want to skip the rest of statements of body for current iteration
		 */
	}
}
