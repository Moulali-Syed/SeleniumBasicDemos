package java1;

public class EIdentifiers {

	/*
	 * identifers is any name , it can be variable name
	 * method name , class name or interface name
	 *
	 * int a =10;
	 * a is indentifier
	 * class ABC{
	 * }
	 *
	 * ABC is identifier
	 *
	 *
	 * Rules:
	 * 1.spaces cannot be used
	 * 2.only _ and $ can be usedd
	 * 3.reserved words cannot be used [50 keywords+ 3 literlas cant be used]
	 * 4.integer cant be used at first position , can be used at later position
	 *
	 *
	 *
	 * Naming convention
	 *
	 * variable:
	 * emp_id , emp_name
	 *
	 * methods
	 * my() , empDetails()  , myJavaDemo()
	 *
	 *
	 * class/interface
	 * My  , MyJava  , MyJavaDemo
	 *
	 */
}
